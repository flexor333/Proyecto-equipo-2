      <p class="lead">“Durante años, nuestro objetivo ha sido descifrar el complejo rompecabezas de la 
        progresión del cáncer de mama para comprender cómo los jugadores interactúan entre sí y a partir de esto, 
        conferir resistencia a la terapia y un crecimiento persistente”, afirmó la autora Weei-Chin Lin, 
        profesora de medicina, hematología y oncología y de biología molecular y celular en Baylor. </p>

        <h2 class="featurette-heading fw-normal lh-1">El camino de la resistencia</h2>

        <p>Para su investigación, los científicos se basaron en dos proteínas celulares conocidas como 14-3-3τ y ERα36, las cuales, en estudios previos, 
            ya se habían implicado en el desarrollo de la resistencia del cáncer de mama a la terapia endocrina. 
            “Al trabajar con un modelo de ratón de cáncer de mama ER+ humano, nos sorprendió descubrir que la sobreexpresión de 14-3-3τ en 
            estos tumores llevó a que todas las células cancerosas se volvieran ER-negativas. 
            Todavía recuerdo el día que vi los datos. El cambio fue espectacular: todos los tumores habían perdido su ER”, 
            explicó Lin, miembro del Centro Oncológico Integral Dan L Duncan. </p>


        <p>Dicho de forma sencilla, la sobreexpresión de estas proteínas provoca que el receptor de estrógeno positivo (ER+) 
                pierda la expresión de ER para convertirse en negativo. Es decir que su expresión hormonal se vuelve negativa, 
                con lo cual las terapias para tratar al tumor son muy limitadas e, incluso, se vuelven infructuosas en varios casos. 
                Cuando esto ocurre, la derivación hacia otros 
                órganos es el camino que eligen los tumores, profundizando y provocando cuadros oncológicos más graves.</p>
        
        <p>Debido a que sería laborioso, lento y costoso estudiar el mecanismo en modelos animales, 
            los investigadores desarrollaron un modelo alternativo. La primera autora, Lidija A. Wilhelms Garan, 
            estudiante del Programa de Posgrado en Biología Celular y Cáncer de Baylor que trabaja en el laboratorio de 
            Lin, desarrolló un modelo esferoide de células de cáncer de mama humano que imita la progresión de ER+ a ER- y 
            proporciona una herramienta experimental muy útil para investigación futura. 
            “En un paciente, un tumor de mama puede tardar años en progresar de ER+ a ER-, en nuestro modelo animal tarda varios meses, 
            pero en nuestro modelo esferoide cambia de ER+ a ER- en 1 o 2 semanas”, confirmó Garan.</p>        

            <p>En ese esquema de trabajo el equipo descubrió que una vez que se sobreexpresa 14-3-3τ en las células cancerosas en las 
                condiciones adecuadas, éstas aumentarán sus niveles de ERα36 y esto es seguido por la pérdida de ER. 
                “También se requieren otros jugadores moleculares, como AKT y GATA3. Es importante destacar que también 
                encontramos que los factores producidos por el microambiente tumoral, que incluye fibroblastos y células inmunitarias 
                que forman parte de la masa tumoral y se cruzan con las células cancerosas, también son esenciales para la progresión de 
                ER+ a ER”, afirmó Garan.</p>  

